#include <minishell.h>

void	expand_heredocs(char **str)
{
	int		i;

	i = 0;
	while ((*str)[i])
	{
		if ((*str)[i] == '$' && ft_isenv((*str)[i + 1]))
			i = select_env(str, i + 1) - 1;
		i++;
	}
}
